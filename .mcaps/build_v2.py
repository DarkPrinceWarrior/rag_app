def module_slide(m):
    fig, ax = new_slide()
    ax.text(0.05, 0.945, f"{m['no']}.   {m['title']}", color=WHITE, fontsize=25, fontweight="bold", va="center")
    ax.text(0.05, 0.945, f"{m['no']}.", color=ACCENT, fontsize=25, fontweight="bold", va="center")
    ax.text(0.05, 0.893, wrap(m["subtitle"], 120).split("\n")[0], color=GRAY, fontsize=12, va="center")
    ax.add_patch(plt.Rectangle((0.05, 0.868), 0.90, 0.003, color=BORDER))
    seen = []
    for st in m["steps"]:
        if st["s"] not in seen and st["s"] in SHOTS: seen.append(st["s"])
    K = max(1, len(seen)); steps = m["steps"]; N = len(steps)
    ex_top = 0.03 + 0.040 + 0.027 * (wrap(m["ex"], 138).count("\n") + 1)
    if K == 1:
        place_image(fig, SHOTS[seen[0]], 0.305, 0.845, 0.53, 0.555)
        rx0 = 0.635; chars = max(30, int((0.965 - rx0) * 1920 / 13.0))
        ytop = 0.835; yb = ex_top + 0.02; per = (ytop - yb) / N
        for i, st in enumerate(steps):
            step_text(ax, st, i + 1, rx0, ytop - i * per, chars, fs_t=12.5, fs_d=10.0)
    else:
        slot = 0.92 / K
        for i, k in enumerate(seen):
            place_image(fig, SHOTS[k], 0.05 + slot * (i + 0.5), 0.85, slot * 0.95, 0.355)
        gap = 0.03; cw = (0.92 - (N - 1) * gap) / N; chars = max(20, int(cw * 1920 / 12.5))
        for i, st in enumerate(steps):
            step_text(ax, st, i + 1, 0.05 + i * (cw + gap), 0.45, chars)
    example_box(ax, m["ex"])
    return fig

