def new_slide():
    fig = plt.figure(figsize=(FW, FH)); fig.patch.set_facecolor(BG)
    ax = fig.add_axes([0, 0, 1, 1]); ax.set_xlim(0, 1); ax.set_ylim(0, 1); ax.axis("off")
    ax.add_patch(plt.Rectangle((0, 0), 1, 1, color=BG, zorder=-10)); return fig, ax

def place_image(fig, path, cx, top, max_w, max_h):
    img = plt.imread(path); ih, iw = img.shape[:2]; asp = (ih / iw) * (FW / FH)
    w = max_w; h = w * asp
    if h > max_h: h = max_h; w = h / asp
    x = cx - w / 2; y = top - h
    a = fig.add_axes([x, y, w, h]); a.imshow(img); a.axis("off")
    a.add_patch(plt.Rectangle((0, 0), 1, 1, transform=a.transAxes, fill=False, edgecolor=BORDER, lw=1.0))
    return x, y, w, h

def wrap(s, n): return "\n".join(textwrap.wrap(s, n)) if s else ""

def step_text(ax, st, n, x, y, chars, fs_t=11.5, fs_d=9.6):
    ax.text(x, y, f"ШАГ {n}", color=ACCENT, fontsize=fs_t - 1.5, fontweight="bold", va="top")
    tt = wrap(st["t"], chars)
    ax.text(x, y - 0.033, tt, color=WHITE, fontsize=fs_t, fontweight="bold", va="top", linespacing=1.15)
    yy = y - 0.033 - 0.030 * (tt.count("\n") + 1) - 0.004
    dd = wrap(st["d"], chars)
    ax.text(x, yy, dd, color=GRAY, fontsize=fs_d, va="top", linespacing=1.4)
    return yy - 0.025 * (dd.count("\n") + 1)

